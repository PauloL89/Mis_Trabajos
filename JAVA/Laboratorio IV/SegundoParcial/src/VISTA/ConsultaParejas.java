     public void cargarTabla()
    {
        ArrayList<Pareja> listado = gestor.consultarParejas();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"PrimerConyuge","SegundoConyuge","Telefono","Domicilio" ,"Ciudad","Provincia", "Estado"});
        for (Pareja dto : listado) 
        {
            modelo.addRow(new Object[]
            {dto.getPrimerConyuge(),dto.getSegundoConyuge(),dto.getTelefono(),dto.getDomcilio(), dto.getCiudad(), dto.getProvincia(), dto.isEstado()});
        }
        tblListado.setModel(modelo);
    }
    
   
